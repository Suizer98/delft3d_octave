%% load the BKL file
BKLfile = load_BKL;

%% split into kustvak and raainummer
kustVak = floor(transectID / 1000000);
raaiNummer = transectID - kustVak*1000000;

%% Preselect area
tBKL = BKLfile(find(BKLfile(:,1)==kustVak),:);

%% sort raynumber
[dum, id] = sort(tBKL(:,2));
tBKL = tBKL(id,:);

%% find BKL
BKL = tBKL(find(tBKL(:,2)==raaiNummer),7);

%% If not determined for that transect interpolate between surrounding transects
if isempty(BKL)
    BKL = interp1(tBKL(:,2),tBKL(:,7),double(raaiNummer),'linear','extrap');
end

